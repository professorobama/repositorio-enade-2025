print("Exemplo de estrutura condicional encadeada - IF Encadeado")
dias_da_semana = int(input("Digite um número de 1 a 7: "))

if dias_da_semana == 1:
    print("Domingo")
elif dias_da_semana == 2:
    print("Segunda-feira")
elif dias_da_semana == 3:
    print("Terça-feira")
elif dias_da_semana == 4:
    print("Quarta-feira")
elif dias_da_semana == 5:
    print("Quinta-feira")
elif dias_da_semana == 6:
    print("Sexta-feira")
elif dias_da_semana == 7:
    print("Sábado")
else:
    print("Não há dia correspondente ao valor digitado!")
